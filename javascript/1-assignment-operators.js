var number = 3
number = number + 4  // 7
number += 4 // 11

number = number - 5 // 6
number -= number // 0

number = number * 4 // 0
number *= number // 0

number = number / 2 // 0
number /= 2 // 0

number = number ** 2 // 1
number **= number // 1

number = number % 5 // 1

// incremental & decremental operator
var value = 10
--value // 9
++value // 10
