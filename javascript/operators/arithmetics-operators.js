var soma = 5 + 2
console.log(soma) // 7

var subtracao = 5 - 2
console.log(subtracao) // 3

var multiplicacao = 5 * 2
console.log(multiplicacao) // 10

var divisaoReal = 5 / 2
console.log(divisaoReal) // 2.5

var restoDivisaoInteira = 5 % 2
console.log(restoDivisaoInteira) // 1

var potencia = 5 ** 2 // 5 ao quadrado
console.log(potencia) // 25

// assim como na matemática, existe a precedência (prioridade) de operadores
var expressao = 5 + 3 / 2
console.log(expressao) // 6.5

var expressao2 = 5 * 10 ** 2;
console.log(expressao2) // 500